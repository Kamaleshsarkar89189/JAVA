// WAP in java to find Odd or even number 

public class Even_Odd {
    public static void main(String[] args) {
        int number = 5;

        if (number % 2 == 0) {
            System.out.println("The given numeber is Even ");
        }else{
            System.out.println("The given numebr is Odd");
        }
    }
}
