public class Five_Number_And_Average {
    public static void main(String[] args) {
        int a = 10, b = 20, c = 30, d = 40, e = 50; 
        int sum = a + b + c + d + e; 
        double average = sum / 5.0; 
        
        System.out.println("Sum: " + sum);
        System.out.println("Average: " + average);
    }
}
